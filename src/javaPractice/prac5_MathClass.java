package javaPractice;

public class prac5_MathClass {
	
	public static void main(String[] args) {
		//메소드가 Static이기 때문에 객체생성 불가
		
		System.out.println(Math.max(5, 8));		//최대값
		System.out.println(Math.min(5, 8));		//최소값
		System.out.println(Math.abs(-10));		//절대값
		System.out.println(Math.random());		//랜덤값 0~1
		System.out.println(Math.sqrt(25));		//제곱근
		//기타 등등
		
		
	}
}
